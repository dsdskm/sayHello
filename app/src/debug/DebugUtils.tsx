export const getRandomName = () => {
  const last = lastName[rand(0, lastName.length - 1)];
  const first = firstName[rand(0, firstName.length - 1)];
  return last + first;
};

const rand = (min: number, max: number) => {
  return Math.floor(Math.random() * (max - min + 1)) + min;
};
